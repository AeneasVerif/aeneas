#!/usr/bin/env python3
"""
aeneas-doc — Generate verification documentation for Aeneas-verified Rust crates.

Usage:
    python -m aeneas_doc generate \\
        --doc-info <doc-info.json> \\
        [--lean-doc <lean-doc.json>] \\
        [--rust-src <path/to/src/>] \\
        --output <output-dir/> \\
        [--title "Crate Name"]

The doc-info JSON is generated by:
    aeneas -doc-info -doc-info-dest doc-info.json <crate.llbc>

The lean-doc JSON is generated by:
    lake exe doc_extract <ModuleName> lean-doc.json
"""

import argparse
import sys
import os

# Allow running as `python -m aeneas_doc` from the scripts/ directory
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from aeneas_doc.generator import generate_docs


def main():
    parser = argparse.ArgumentParser(
        prog="aeneas-doc",
        description="Generate verification documentation for Aeneas-verified Rust crates.",
    )
    sub = parser.add_subparsers(dest="command")

    gen = sub.add_parser("generate", help="Generate HTML documentation")
    gen.add_argument("--doc-info", required=True,
                     help="Path to Aeneas doc-info JSON (from `aeneas -doc-info`)")
    gen.add_argument("--lean-doc", default=None,
                     help="Path to Lean doc-extract JSON (from `lake exe doc_extract`)")
    gen.add_argument("--rust-src", default=None,
                     help="Path to Rust source directory")
    gen.add_argument("--lean-src", default=None,
                     help="Path to Lean project root (for Lean source pages)")
    gen.add_argument("--output", "-o", required=True,
                     help="Output directory for generated HTML")
    gen.add_argument("--title", default=None,
                     help="Documentation title (defaults to crate name)")
    gen.add_argument("--open", action="store_true",
                     help="Open the generated docs in a browser")

    args = parser.parse_args()

    if args.command == "generate":
        generate_docs(
            doc_info_path=args.doc_info,
            lean_doc_path=args.lean_doc,
            rust_src_dir=args.rust_src,
            lean_src_dir=args.lean_src,
            output_dir=args.output,
            title=args.title,
        )
        if args.open:
            import webbrowser
            webbrowser.open(f"file://{os.path.abspath(args.output)}/index.html")
    else:
        parser.print_help()
        sys.exit(1)


if __name__ == "__main__":
    main()
